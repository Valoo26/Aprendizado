package locadora;

/**
 *
 * @author luizv
 */
public class Filme {
    String nomefilme;
    String genero;

    public Filme(String nomefilme, String genero) {
        this.nomefilme = nomefilme;
        this.genero = genero;
    }

    public String getNomefilme() {
        return nomefilme;
    }

    public void setNomefilme(String nomefilme) {
        this.nomefilme = nomefilme;
    }

    public String getGenero() {
        return genero;
    }

    public void setGenero(String genero) {
        this.genero = genero;
    }
    
    
    
  
    
}
